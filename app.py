def main():
    data, is_synthetic = get_data()
    render_header(is_synthetic)

    budget_pct, active_strategies, beta_ucb, beta_div = render_sidebar(data)

    if not active_strategies:
        st.warning("Select at least one strategy in the sidebar.")
        return

    # Section 1: Metric trajectories (Panels A + B)
    render_metric_trajectories(data, active_strategies)

    st.divider()

    # Section 2: Two panels side by side — latent space + bias distribution
    col_left, col_right = st.columns([3, 2])

    with col_left:
        render_latent_space(data, budget_pct, active_strategies)

    with col_right:
        render_selection_bias(data, budget_pct, active_strategies)

    # Section 3: Research context
    render_research_context()

    # Footer
    st.caption(
        "Norman et al. 2019 · K562 CRISPRi screen · 105 single-gene knockouts · "
        "GP surrogate in PCA latent space · "
        "Neha Mungale · MSc AI for Biomedicine, UCL"
    )


if __name__ == "__main__":
    main()
