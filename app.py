@st.cache_data
def make_synthetic_data():
    """
    Synthetic demo data that reproduces the paper's reported numbers exactly.
    Used when active_learning_results.json is not present.
    """
    rng = np.random.RandomState(42)
    n_perts = 105
    n_rounds = 18  # 0..17

    # Simulate PCA scatter with two clusters (high- and low-effect)
    high_n = 35
    low_n = n_perts - high_n
    pca_x = np.concatenate([rng.normal(3, 0.8, high_n), rng.normal(-1, 1.2, low_n)])
    pca_y = np.concatenate([rng.normal(2, 0.6, high_n), rng.normal(-0.5, 1.0, low_n)])
    effect_sizes = np.concatenate([rng.uniform(3.0, 8.0, high_n), rng.uniform(0.3, 2.5, low_n)])
    gene_names = [f"GENE{i:03d}" for i in range(n_perts)]

    perturbations = [
        {"id": gene_names[i], "pca_x": round(float(pca_x[i]), 4),
         "pca_y": round(float(pca_y[i]), 4), "effect_size": round(float(effect_sizes[i]), 4)}
        for i in range(n_perts)
    ]

    budgets = np.linspace(9.5, 90.5, n_rounds)

    def make_trajectory(final_val, peak_val=None, peak_at=5, shape="stable"):
        vals = []
        for i, b in enumerate(budgets):
            if shape == "stable":
                v = 0.65 + (final_val - 0.65) * (b / 90)
                v += rng.normal(0, 0.01)
            elif shape == "collapse":
                if i <= peak_at:
                    v = 0.70 + 0.24 * (i / peak_at)
                else:
                    v = peak_val - (peak_val - final_val) * ((i - peak_at) / (n_rounds - peak_at))
                v += rng.normal(0, 0.01)
            elif shape == "rise":
                v = 0.55 + (final_val - 0.55) * (i / (n_rounds - 1)) ** 0.7
                v += rng.normal(0, 0.01)
            vals.append(round(float(np.clip(v, 0.4, 1.0)), 4))
        return vals

    spearman = {
        "random":      make_trajectory(0.99, shape="rise"),
        "ucb_only":    make_trajectory(0.70, peak_val=0.94, peak_at=4, shape="collapse"),
        "ucb_diverse": make_trajectory(0.88, peak_val=0.94, peak_at=5, shape="collapse"),
    }
    deg = {
        "random":      make_trajectory(0.70, shape="rise"),
        "ucb_only":    make_trajectory(0.57, shape="stable"),
        "ucb_diverse": make_trajectory(0.63, shape="stable"),
    }

    # Build synthetic selection sequences
    all_idx = list(range(n_perts))
    high_idx = list(range(high_n))
    low_idx = list(range(high_n, n_perts))

    def make_rounds(strategy):
        rounds = []
        if strategy == "ucb_only":
            # Preferentially selects high-effect indices first
            order = high_idx + rng.permutation(low_idx).tolist()
        elif strategy == "ucb_diverse":
            order = rng.permutation(all_idx).tolist()
            order = sorted(order, key=lambda i: -effect_sizes[i] * 0.7 + rng.uniform(0, 1))
        else:
            order = rng.permutation(all_idx).tolist()

        seed = order[:10]
        step = 5
        for i, b in enumerate(budgets):
            n_obs = min(10 + i * step, n_perts)
            obs = order[:n_obs]
            rounds.append({
                "budget_pct": round(float(b), 2),
                "observed_indices": obs,
                "spearman_r": spearman[strategy][i],
                "deg_recovery": deg[strategy][i],
            })
        return {"seed_indices": seed, "rounds": rounds}

    strategies_data = {s: {"repeats": [make_rounds(s)]} for s in ["random", "ucb_only", "ucb_diverse"]}

    # Selection bias at 50%
    bias = {}
    for s in ["random", "ucb_only", "ucb_diverse"]:
        obs = strategies_data[s]["repeats"][0]["rounds"][8]["observed_indices"]
        bias[s] = {
            "selected_indices": obs,
            "effect_sizes": [round(float(effect_sizes[i]), 4) for i in obs],
        }

    return {
        "metadata": {
            "n_perturbations": n_perts,
            "seed_size": 10,
            "budget_step": 5,
            "n_rounds": 17,
            "n_repeats": 1,
            "beta_ucb": 1.0,
            "beta_div": 1.5,
            "dataset": "Synthetic demo (Norman et al. 2019 numbers reproduced)",
        },
        "perturbations": perturbations,
        "strategies": strategies_data,
        "selection_bias_rep0": bias,
    }


def get_data():
    data = load_data()
    if data is None:
        return make_synthetic_data(), True  # True = synthetic
    return data, False


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def mean_across_repeats(data, strategy, metric):
    """Return (budgets[], mean[], std[]) averaged across all repeats."""
    repeats = data["strategies"][strategy]["repeats"]
    n_rounds = len(repeats[0]["rounds"])
    budgets = [r["budget_pct"] for r in repeats[0]["rounds"]]
    vals = np.array([
        [rep["rounds"][i][metric] for i in range(n_rounds)]
        for rep in repeats
    ])
    return budgets, vals.mean(axis=0).tolist(), vals.std(axis=0).tolist()


