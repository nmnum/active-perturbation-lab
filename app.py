def render_selection_bias(data, budget_pct, active_strategies):
    st.subheader("Selection bias distribution")
    st.caption(
        f"Effect-size distribution of perturbations selected by each strategy at "
        f"**{budget_pct}% budget** (repeat 0). "
        "UCB preferentially selects high-effect perturbations, leaving the low-effect "
        "regime unobserved — the 'smoking gun' for the Spearman R collapse."
    )

    # Use pre-computed 50% data from JSON for the histogram; adjust label
    bias = data["selection_bias_rep0"]

    fig = go.Figure()
    bins = np.linspace(0, 9, 19)

    for strategy in active_strategies:
        if strategy not in bias:
            continue
        effs = bias[strategy]["effect_sizes"]
        col = COLOURS[strategy]
        lab = LABELS[strategy]

        counts, edges = np.histogram(effs, bins=bins, density=True)
        fig.add_trace(go.Bar(
            x=((edges[:-1] + edges[1:]) / 2).tolist(),
            y=counts.tolist(),
            name=lab,
            marker_color=col,
            opacity=0.55,
            width=float(edges[1] - edges[0]) * 0.85,
        ))

    mean_vals = {s: round(float(np.mean(bias[s]["effect_sizes"])), 2)
                 for s in active_strategies if s in bias}

    fig.update_layout(
        barmode="overlay",
        xaxis_title="Perturbation effect size (L2 distance from control)",
        yaxis_title="Density",
        height=320,
        legend=dict(orientation="h", y=-0.28, x=0.5, xanchor="center"),
        margin=dict(t=20, b=60, l=60, r=20),
        plot_bgcolor="white", paper_bgcolor="rgba(0,0,0,0)",
    )
    fig.update_xaxes(showgrid=True, gridcolor="#eeeeee")
    fig.update_yaxes(showgrid=True, gridcolor="#eeeeee")

    st.plotly_chart(fig, width='stretch')

    if mean_vals:
        st.caption("Mean effect size of selected perturbations at 50% budget: " +
                   "  |  ".join(f"**{LABELS[s]}**: {v}" for s, v in mean_vals.items()))


