def render_metric_trajectories(data, active_strategies):
    st.subheader("Metric trajectories across budget")
    st.caption(
        "**Left:** Spearman R between predicted and true effect sizes (unobserved perturbations). "
        "**Right:** Fraction of true top-20 DEGs correctly recovered. "
        "Shaded bands = ±1 SD across repeats."
    )

    fig = make_subplots(
        rows=1, cols=2,
        subplot_titles=["A  Effect-size ranking (Spearman R)",
                        "B  Landscape coverage (top-20 DEG recovery)"],
        horizontal_spacing=0.10,
    )

    for strategy in active_strategies:
        budgets, mean_spe, std_spe = mean_across_repeats(data, strategy, "spearman_r")
        _, mean_deg, std_deg = mean_across_repeats(data, strategy, "deg_recovery")
        col = COLOURS[strategy]
        lab = LABELS[strategy]

        mean_spe = np.array(mean_spe)
        std_spe = np.array(std_spe)
        mean_deg = np.array(mean_deg)
        std_deg = np.array(std_deg)

        # Spearman R
        fig.add_trace(go.Scatter(
            x=budgets, y=mean_spe.tolist(),
            name=lab, line=dict(color=col, width=2.5, dash=DASHES[strategy]),
            legendgroup=strategy, showlegend=True,
        ), row=1, col=1)
        fig.add_trace(go.Scatter(
            x=budgets + budgets[::-1],
            y=(mean_spe + std_spe).tolist() + (mean_spe - std_spe).tolist()[::-1],
            fill="toself", fillcolor=col, opacity=0.12,
            line=dict(width=0), showlegend=False, legendgroup=strategy,
            hoverinfo="skip",
        ), row=1, col=1)

        # DEG recovery
        fig.add_trace(go.Scatter(
            x=budgets, y=mean_deg.tolist(),
            name=lab, line=dict(color=col, width=2.5, dash=DASHES[strategy]),
            legendgroup=strategy, showlegend=False,
        ), row=1, col=2)
        fig.add_trace(go.Scatter(
            x=budgets + budgets[::-1],
            y=(mean_deg + std_deg).tolist() + (mean_deg - std_deg).tolist()[::-1],
            fill="toself", fillcolor=col, opacity=0.12,
            line=dict(width=0), showlegend=False, legendgroup=strategy,
            hoverinfo="skip",
        ), row=1, col=2)

    # 30% budget reference line
    for col_idx in [1, 2]:
        fig.add_vline(x=30, line_dash="dot", line_color="#999999",
                      line_width=1, row=1, col=col_idx)

    fig.add_annotation(x=31, y=0.43, text="30% budget", showarrow=False,
                       font=dict(size=11, color="#888888"), xref="x", yref="y")

    fig.update_xaxes(title_text="Experimental budget used (%)", range=[8, 95])
    fig.update_yaxes(title_text="Spearman R", range=[0.38, 1.05], row=1, col=1)
    fig.update_yaxes(title_text="DEG recovery fraction", range=[0.38, 0.78], row=1, col=2)
    fig.update_layout(
        height=400, legend=dict(orientation="h", y=-0.22, x=0.5, xanchor="center"),
        margin=dict(t=50, b=60, l=60, r=20),
        plot_bgcolor="white", paper_bgcolor="rgba(0,0,0,0)",
    )
    fig.update_xaxes(showgrid=True, gridcolor="#eeeeee")
    fig.update_yaxes(showgrid=True, gridcolor="#eeeeee")

    st.plotly_chart(fig, width='stretch')

    # UCB collapse callout
    if "ucb_only" in active_strategies:
        budgets_u, mean_spe_u, _ = mean_across_repeats(data, "ucb_only", "spearman_r")
        idx30 = min(range(len(budgets_u)), key=lambda i: abs(budgets_u[i] - 30))
        idx90 = len(budgets_u) - 1
        peak = round(mean_spe_u[idx30], 3)
        final = round(mean_spe_u[idx90], 3)

        col1, col2, col3 = st.columns(3)
        col1.metric("UCB Spearman R @ 30% budget", peak, help="Peak accuracy before collapse")
        col2.metric("UCB Spearman R @ 90% budget", final,
                    delta=f"{round(final - peak, 3)}", delta_color="inverse",
                    help="After selection bias exhausts high-effect perturbations")
        col3.metric("Drop", f"{round(peak - final, 3)}", delta=None,
                    help="The UCB collapse — low-effect regime left unobserved")


