def render_latent_space(data, budget_pct, active_strategies):
    st.subheader("PCA latent space — acquisition playground")
    st.caption(
        "Each point is a perturbation, positioned by its first two principal components "
        "of mean expression. Colour = true effect size (gray → coral = low → high). "
        "**Ringed points** = selected by that strategy at the chosen budget. "
        "Watch how UCB clusters in the high-effect corner while Diversity spreads out."
    )

    perts = data["perturbations"]
    df = pd.DataFrame(perts)

    # Find the round closest to the requested budget for each strategy
    def get_observed_at_budget(strategy, target_pct):
        repeats = data["strategies"][strategy]["repeats"]
        rounds = repeats[0]["rounds"]  # use repeat 0
        closest = min(rounds, key=lambda r: abs(r["budget_pct"] - target_pct))
        return set(closest["observed_indices"])

    n_strategies = len(active_strategies)
    if n_strategies == 0:
        st.warning("Select at least one strategy in the sidebar.")
        return

    cols = st.columns(n_strategies)
    for col_ui, strategy in zip(cols, active_strategies):
        observed_set = get_observed_at_budget(strategy, budget_pct)

        df["selected"] = [i in observed_set for i in range(len(df))]
        df["marker_size"] = df["selected"].map({True: 10, False: 6})
        df["alpha"] = df["selected"].map({True: 1.0, False: 0.35})
        df["label"] = df["selected"].map({True: "selected", False: "unobserved"})

        fig = go.Figure()

        # Unobserved — small, muted
        unobs = df[~df["selected"]]
        fig.add_trace(go.Scatter(
            x=unobs["pca_x"], y=unobs["pca_y"],
            mode="markers",
            marker=dict(
                size=6,
                color=unobs["effect_size"],
                colorscale=[[0, "#D3D1C7"], [1, "#D85A30"]],
                cmin=float(df["effect_size"].min()),
                cmax=float(df["effect_size"].max()),
                opacity=0.35,
                line=dict(width=0),
            ),
            text=unobs["id"],
            hovertemplate="%{text}<br>effect size: %{marker.color:.2f}<extra></extra>",
            showlegend=False,
        ))

        # Selected — larger, full opacity, ring
        obs = df[df["selected"]]
        fig.add_trace(go.Scatter(
            x=obs["pca_x"], y=obs["pca_y"],
            mode="markers",
            marker=dict(
                size=11,
                color=obs["effect_size"],
                colorscale=[[0, "#D3D1C7"], [1, "#D85A30"]],
                cmin=float(df["effect_size"].min()),
                cmax=float(df["effect_size"].max()),
                opacity=1.0,
                line=dict(width=2, color=COLOURS[strategy]),
                showscale=True,
                colorbar=dict(
                    title=dict(
                        text="Effect Size",
                        font=dict(size=10) 
                    ),
                    thickness=10,
                    len=0.7,
                    tickfont=dict(size=9),
                ) if strategy == active_strategies[-1] else None,
            ),
            text=obs["id"],
            hovertemplate="%{text}<br>effect size: %{marker.color:.2f}<extra></extra>",
            showlegend=False,
        ))

        n_sel = len(observed_set)
        pct_actual = round(n_sel / len(df) * 100, 1)

        fig.update_layout(
            title=dict(
                text=f"<b>{LABELS[strategy]}</b><br>"
                     f"<span style='font-size:11px;color:#888'>{n_sel} selected ({pct_actual}%)</span>",
                font=dict(size=13),
            ),
            height=340,
            margin=dict(t=60, b=30, l=30, r=10),
            xaxis=dict(title="PC1", showgrid=True, gridcolor="#eeeeee", zeroline=False),
            yaxis=dict(title="PC2", showgrid=True, gridcolor="#eeeeee", zeroline=False),
            plot_bgcolor="white", paper_bgcolor="rgba(0,0,0,0)",
        )
        col_ui.plotly_chart(fig, width='stretch')


